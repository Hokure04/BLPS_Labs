package blps.labs.factory;

import blps.labs.provider.RegistrationProvider;
import com.google.auto.service.AutoService;
import org.keycloak.Config;
import org.keycloak.models.KeycloakSession;
import org.keycloak.models.KeycloakSessionFactory;
import org.keycloak.services.resource.RealmResourceProvider;
import org.keycloak.services.resource.RealmResourceProviderFactory;

@AutoService(RealmResourceProviderFactory.class)
public class RegistrationProviderFactory implements RealmResourceProviderFactory {

    private final String PROVIDER_ID = "blps-registration";

    @Override
    public RealmResourceProvider create(KeycloakSession session) {
        return new RegistrationProvider(session);
    }

    @Override
    public void init(Config.Scope config) {

    }

    @Override
    public void postInit(KeycloakSessionFactory factory) {

    }

    @Override
    public void close() {

    }

    @Override
    public String getId() {
        return PROVIDER_ID;
    }
}
